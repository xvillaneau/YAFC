
class Machine:
    """
    Mixin class for items that are machines that can be used for
    transforming and/or producing other items.
    """
    pass


class Miner(Machine):
    """
    Mining machines.
    """
    pass


class Assembler(Machine):
    """
    Assembling machines.
    """
    pass


class Furnace(Machine):
    """
    Furnaces.
    """
    pass


class ChemicalPlant(Machine):
    """
    Chemical Plants.
    """
    pass
